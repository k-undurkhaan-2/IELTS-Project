import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { getBundleProfile } from './bundle-manifest.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, '..');

const VIP_COMMAND = 'node scripts/build-bundles.mjs --profile vip --output-root "ListeningPractice/vip special"';

function isPathInside(parent, child) {
    const parentPath = path.resolve(parent);
    const childPath = path.resolve(child);
    const relative = path.relative(parentPath, childPath);
    return relative === '' || (relative && !relative.startsWith('..') && !path.isAbsolute(relative));
}

function readOptionValue(args, index, optionName) {
    const value = args[index + 1];
    if (!value || value.startsWith('--')) {
        throw new Error(`Missing value for ${optionName}`);
    }
    return value;
}

export function parseBuildArgs(args) {
    let profileName = 'default';
    let outputRootArgument = null;

    for (let index = 0; index < args.length; index += 1) {
        const arg = args[index];
        if (arg === '--profile') {
            profileName = String(readOptionValue(args, index, '--profile')).trim();
            index += 1;
        } else if (arg.startsWith('--profile=')) {
            profileName = arg.slice('--profile='.length).trim();
            if (!profileName) {
                throw new Error('Missing value for --profile');
            }
        } else if (arg === '--output-root') {
            outputRootArgument = readOptionValue(args, index, '--output-root');
            index += 1;
        } else if (arg.startsWith('--output-root=')) {
            outputRootArgument = arg.slice('--output-root='.length);
            if (!outputRootArgument) {
                throw new Error('Missing value for --output-root');
            }
        } else {
            throw new Error(`Unknown build-bundles argument: ${arg}`);
        }
    }

    const profile = getBundleProfile(profileName);
    if (profile.name === 'vip' && outputRootArgument === null) {
        throw new Error(`VIP output root must be explicit. Run: ${VIP_COMMAND}`);
    }

    const outputRoot = outputRootArgument === null
        ? root
        : path.resolve(root, outputRootArgument);
    const expectedOutputRoot = path.resolve(root, profile.outputRoot);
    if (!isPathInside(root, outputRoot) || outputRoot !== expectedOutputRoot) {
        throw new Error(
            `Profile ${profile.name} must write to ${profile.outputRoot}. `
            + `Run: ${profile.name === 'vip' ? VIP_COMMAND : 'node scripts/build-bundles.mjs'}`
        );
    }

    return { profile, outputRoot };
}

export function normalizeEol(value) {
    return String(value).replace(/\r\n/g, '\n').replace(/\r/g, '\n');
}

export function normalizeSourceText(value) {
    return normalizeEol(value)
        .split('\n')
        .map((line) => line.replace(/[ \t]+$/g, ''))
        .join('\n')
        .replace(/\s*$/, '\n');
}

function readSource(relativePath) {
    const absolutePath = path.join(root, relativePath);
    if (!fs.existsSync(absolutePath)) {
        throw new Error(`Missing bundle input: ${relativePath}`);
    }
    return normalizeSourceText(fs.readFileSync(absolutePath, 'utf8'));
}

export function renderBundle(profileName, inputs, sourceReader = readSource) {
    const sections = inputs.map((inputPath) => {
        const source = sourceReader(inputPath);
        return [
            `\n/* ===== ${inputPath} ===== */`,
            source
        ].join('\n');
    });

    const provided = JSON.stringify(inputs, null, 4);
    return [
        `/* Generated by scripts/build-bundles.mjs${profileName === 'vip' ? ' --profile vip' : ''}. Do not edit by hand. */`,
        ...sections,
        '\n/* ===== bundle provided script markers ===== */',
        '(function markBundleProvided(global) {',
        '    if (global.AppLazyLoader && typeof global.AppLazyLoader.markProvided === "function") {',
        `        global.AppLazyLoader.markProvided(${provided});`,
        '    }',
        '})(typeof window !== "undefined" ? window : this);',
        ''
    ].join('\n');
}

function removeRetiredOutputs(profile, outputRoot) {
    const removed = [];
    for (const outputPath of profile.retiredOutputs) {
        const absoluteOutput = path.resolve(outputRoot, outputPath);
        if (!isPathInside(outputRoot, absoluteOutput)) {
            throw new Error(`Refusing to remove retired output outside profile root: ${outputPath}`);
        }
        if (!fs.existsSync(absoluteOutput)) {
            continue;
        }
        if (!fs.statSync(absoluteOutput).isFile()) {
            throw new Error(`Retired bundle output is not a file: ${outputPath}`);
        }
        fs.unlinkSync(absoluteOutput);
        removed.push(outputPath);
    }
    return removed;
}

function writeBundleIfChanged(absoluteOutput, content) {
    if (fs.existsSync(absoluteOutput)) {
        const existing = fs.readFileSync(absoluteOutput, 'utf8');
        if (normalizeEol(existing) === content) {
            return false;
        }
    }
    fs.mkdirSync(path.dirname(absoluteOutput), { recursive: true });
    fs.writeFileSync(absoluteOutput, content, 'utf8');
    return true;
}

function assertNoUndeclaredOutputs(profile, outputRoot) {
    if (profile.allowUndeclaredOutputs) {
        return;
    }
    const bundleDirectory = path.join(outputRoot, 'js', 'bundles');
    const declared = new Set(profile.bundles.map((bundle) => bundle.outputPath));
    const actual = fs.existsSync(bundleDirectory)
        ? fs.readdirSync(bundleDirectory, { withFileTypes: true })
            .filter((entry) => entry.isFile() && entry.name.endsWith('.bundle.js'))
            .map((entry) => `js/bundles/${entry.name}`)
        : [];
    const undeclared = actual.filter((outputPath) => !declared.has(outputPath)).sort();
    if (undeclared.length > 0) {
        throw new Error(`Undeclared bundle outputs for profile ${profile.name}: ${undeclared.join(', ')}`);
    }
}

export function runBuild(buildOptions) {
    const { profile, outputRoot } = buildOptions;
    const removed = removeRetiredOutputs(profile, outputRoot);
    removed.forEach((outputPath) => console.log(`${outputPath}: retired output removed`));

    const written = [];
    for (const bundle of profile.bundles) {
        const absoluteOutput = path.join(outputRoot, bundle.outputPath);
        const content = renderBundle(profile.name, bundle.inputs);
        const changed = writeBundleIfChanged(absoluteOutput, content);
        if (changed) {
            written.push(bundle.outputPath);
        }
        console.log(`${bundle.outputPath}: ${bundle.inputs.length} files (${changed ? 'written' : 'unchanged'})`);
    }
    assertNoUndeclaredOutputs(profile, outputRoot);
    return { profile: profile.name, removed, written };
}

if (process.argv[1] && path.resolve(process.argv[1]) === __filename) {
    runBuild(parseBuildArgs(process.argv.slice(2)));
}
